// ─── Comparison UI state ───
let currentSentenceIdx = 0;

function renderComparison() {
    const lang = document.getElementById("cl-lang").value;
    const idx = currentSentenceIdx;
    const isReverse = lang.startsWith("rev-");
    const originalEl = document.getElementById("cl-original");

    if (isReverse) {
        const sourceLang = lang.replace("rev-", "").toUpperCase().replace("ZH-HANS", "ZH");
        originalEl.setAttribute("data-source-lang", sourceLang);
        originalEl.textContent = reverseComparisonSentences[lang]?.[idx] || "";
        const data = reverseComparisonData[lang];
        if (!data) return;
        document.getElementById("cl-apple").textContent = data.apple[idx] || "";
        document.getElementById("cl-deepl").textContent = data.deepl[idx] || "";
        document.getElementById("cl-google").textContent = data.google[idx] || "";
        document.getElementById("cl-azure").textContent = data.azure[idx] || "";
    } else {
        originalEl.setAttribute("data-source-lang", "EN");
        originalEl.textContent = comparisonSentences[idx];
        const data = comparisonData[lang];
        if (!data) return;
        document.getElementById("cl-apple").textContent = data.apple[idx] || "";
        document.getElementById("cl-deepl").textContent = data.deepl[idx] || "";
        document.getElementById("cl-google").textContent = data.google[idx] || "";
        document.getElementById("cl-azure").textContent = data.azure[idx] || "";
    }
}

function initComparison() {
    const langSelect = document.getElementById("cl-lang");
    const tabs = document.querySelectorAll(".cl-tab");

    if (!langSelect || !tabs.length) return;

    langSelect.addEventListener("change", renderComparison);

    tabs.forEach((tab) => {
        tab.addEventListener("click", () => {
            tabs.forEach((t) => t.classList.remove("active"));
            tab.classList.add("active");
            currentSentenceIdx = parseInt(tab.dataset.idx, 10);
            renderComparison();
        });
    });

    renderComparison();
}

// ─── i18n ───
function applyEnginesLang(lang) {
    const t = enginesTranslations[lang];
    if (!t) return;
    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (t[key]) el.innerHTML = t[key];
    });
}

document.addEventListener("DOMContentLoaded", () => {
    const saved = localStorage.getItem("lang");
    const browserLang = navigator.language.startsWith("ko") ? "ko" : "en";
    const lang = saved || browserLang;
    applyEnginesLang(lang);
    initComparison();
});

// Override toggleLang for this page
window.toggleLang = function () {
    const current = localStorage.getItem("lang") || "en";
    const next = current === "en" ? "ko" : "en";
    localStorage.setItem("lang", next);
    document.documentElement.lang = next;
    applyEnginesLang(next);

    if (typeof setLang === "function") {
        setLang(next);
    }
};
